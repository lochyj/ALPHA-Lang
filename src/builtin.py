import math

# Math
def sin(num):
    num = int(num);
    return math.sin(num);

def cos(num):
    num = int(num);
    return math.cos(num);

def tan(num):
    num = int(num);
    return math.sin(num)/math.cos(num);

def log(num):
    num = int(num);
    return math.log(num);

def log10(num):
    num = int(num);
    return math.log10(num);

def sqrt(num):
    num = int(num);
    return math.sqrt(num);

def ceil(num):
    num = int(num);
    return math.ceil(num);

def abs(num):
    num = int(num);
    return math.fabs(num);

